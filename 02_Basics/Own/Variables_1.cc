#include <iostream>

int main()
{
    // 1 Byte
    bool my_bool_1 = true;
    bool my_bool_2 = false;

    // 1 Byte = 8 Bit (2^8 = 256)
    char my_char = 10;

    // 2 Byte = 16 Bit (2 ^ 16 ~65.000)
    short my_short = 300;

    // 4 Byte = 32 Bit (2^32 ~ 4.000.0000.000)
    int my_int = 100000;

    // 8 BYte 064 Bit (2^64)
    long long my_long = 5000000000;

    // 4 Byte = 32 Bit (2^32 ~ 4.000.0000.000)
    float my_float = 10.0;

    // 8 BYte 064 Bit (2^64)
    double my_dbl = 10.0;

     return 0;
}
